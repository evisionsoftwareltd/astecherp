	private boolean queryCheck(String query,Session session){

		List list=session.createSQLQuery(query).list();
		if(list.isEmpty()){
			return true;
		}
		else{
			return false;
		}
	}

	private void reportShow()
	{
		String query=null;
		String activeFlag = null;
		
		if(chkpdf.booleanValue()==true)
			type=1;
		else
			type=0;

		String supplyID = "";

		if(chkall.booleanValue()==true){
			supplyID="%";
		}else{
			supplyID = cmbSupplierName.getValue().toString();
		}

		try{

			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			Transaction tx = session.beginTransaction();

			String asOn = new SimpleDateFormat("yyyy-MM-dd").format(asOnDate.getValue());

			HashMap hm = new HashMap();
			hm.put("company", sessionBean.getCompany());
			hm.put("address", sessionBean.getCompanyAddress());
			hm.put("phone", sessionBean.getCompanyContact());
			hm.put("username", sessionBean.getUserName()+"  "+sessionBean.getUserIp());
			hm.put("logo", sessionBean.getCompanyLogo());
			hm.put("user", sessionBean.getUserName());

			hm.put("asOnDate", new SimpleDateFormat("dd-MM-yyyy").format(asOnDate.getValue()));
			query=	" select *, (select  STUFF((SELECT ', ' + CAST(ChallanNo AS VARCHAR(50)) [text()] from tbRawPurchaseInfo where poNo = '0' FOR XML PATH(''), TYPE).value('.','VARCHAR(MAX)'),1,2,' ')) as ChallanNo, (select  STUFF((SELECT ', ' + CAST(convert(varchar(500),challanDate,105) AS VARCHAR(50)) [text()] from tbRawPurchaseInfo where poNo = '0' FOR XML PATH(''), TYPE).value('.','VARCHAR(MAX)'),1,2,' ')) as challanDate from" +
					" (select  ap.poNo, (select ISNULL(SUM(pos.qty),0) from tbRawPurchaseOrderDetails as pos where poNo like ap.poNo ) as poqty ," +
					" ( select ISNULL(SUM(b.Qty),0)  from tbRawPurchaseInfo a " +
					" inner join " +
					" tbRawPurchaseDetails as b on  a.ReceiptNo=b.ReceiptNo " +
					" where a.poNo like ap.poNo  ) as Receiveqty,ap.poDate,ap.lastDate ,COUNT(b.productId)as 'No Of Item' ,ap.supplierId,sp.supplierName  " +
					" from tbRawPurchaseOrderInfo ap" +
					" inner join" +
					" tbRawPurchaseOrderDetails b" +
					" on" +
					" ap.poNo=b.poNo" +
					" inner join" +
					" tbSupplierInfo sp" +
					" on sp.supplierId=ap.supplierId" +
					" where ap.poNo not in(select poNo from tbRawPurchaseInfo as d) or ( ap.poNo  in(select poNo from tbRawPurchaseInfo) and  b.qty<" +
					" (select SUM(c.Qty) from tbRawPurchaseDetails c  where c.ReceiptNo = (select ReceiptNo from tbRawPurchaseInfo k where k.poNo=ap.poNo )) )" +
					" group by ap.poNo,ap.poDate,ap.lastDate ,ap.supplierId,sp.supplierName" +
					" ) as pendingPo where  pendingPo.supplierId like '"+supplyID+"' and  CONVERT(date,pendingPo.poDate,105) <='"+new SimpleDateFormat("yyyy-MM-dd").format(asOnDate.getValue())+"'" +
					" order by pendingPo.poNo";

			System.out.println(query);
			hm.put("sql", query);

			if(!queryCheck(query,session))
			{
				Window win = new ReportViewerNew(hm,"report/raw/rptPurchaseOrderRegister.jasper",
						this.getWindow().getApplication().getContext().getBaseDirectory()+"".replace("\\","/")+"/VAADIN/rpttmp",
						this.getWindow().getApplication().getURL()+"VAADIN/rpttmp",false,
						this.getWindow().getApplication().getURL()+"VAADIN/applet",type);
				win.setCaption("Report : R/M Purchase Order Register");
				this.getParent().getWindow().addWindow(win);
			}
			else{
				this.getParent().showNotification("There are no Data!!",Notification.TYPE_WARNING_MESSAGE);
			}
		}
		catch(Exception exp){

			this.getParent().showNotification("Error "+exp,Notification.TYPE_ERROR_MESSAGE);

		}
	}
