	private String subGroupIdWork(){

		String id=null;
		String sql="select isnull(max(cast(SUBSTRING(stepid,CHARINDEX('-',stepId)+1,len(stepId)-CHARINDEX('-',stepId)) as int)),0)+1 as cat " +
				"from tbProductionStep where productionTypeId like  '"+cmbGroupName.getValue()+"'";
		Iterator<?>iter=dbService(sql);
		if(iter.hasNext()){
			Object element=(Object) iter.next();
			id=element.toString();
			return id;
		}
		return "";
	}
	private void findButtonEvent() 
	{
		Window win = new ProductionStepProcessFindWindow(sessionBean, txtSubGroupID);
		win.addListener(new Window.CloseListener() 
		{
			public void windowClose(CloseEvent e) 
			{
				if (txtSubGroupID.getValue().toString().length() > 0)
				{
					txtClear();
					findInitialise(txtSubGroupID.getValue().toString());
				}
			}
		});

		this.getParent().addWindow(win);
	}

	private void findInitialise(String txtSubGroupId) 
	{

		String sql="select stepId,stepName,ProductionTypeId from tbProductionStep Where stepId = '"+txtSubGroupId+"'";
		Iterator<?>iter=dbService(sql);
		if(iter.hasNext()){
			Object[] element = (Object[]) iter.next();
			txtSubGroupName.setValue(element[1]);
			cmbGroupName.setValue(element[2].toString());
			trSubGroupId.setValue(element[0]);
		}
	}

	public void gradeFormLink()
	{
		Window win = new ProductionType(sessionBean);

		win.setStyleName("cwindow");
		win.setModal(true);
		win.addListener(new Window.CloseListener() 
		{
			public void windowClose(CloseEvent e) 
			{
				cmbCategoryData();
				System.out.println("Category Form");
			}
		});
		this.getParent().addWindow(win);
	}

	private void cmbCategoryData()
	{
		String query = "select productTypeId,ProductTypeName from tbProductionType  ";
		Iterator<?>iter=dbService(query);
		while(iter.hasNext()){
			Object[] element = (Object[]) iter.next();
			if (element[0] != null) {
				cmbGroupName.addItem(element[0]);
				cmbGroupName.setItemCaption(element[0], (String) element[1]);			
			}
		}
	}

	private void updateButtonEvent()
	{
		if(!txtSubGroupName.getValue().toString().isEmpty())
		{
			isUpdate = true;
			btnIni(false);
			componentIni(false);
			trSubGroupId.setEnabled(false);
			cmbGroupName.setEnabled(false);
		}
		else
		{
			this.getParent().showNotification("Update Failed","There are no data for update.",Notification.TYPE_WARNING_MESSAGE);
		}
	}

	private void refreshButtonEvent() 
	{
		isUpdate=false;
		componentIni(true);
		btnIni(true);
		txtClear();
	}

	public void updateData() 
	{
		Transaction tx=null;
		Session session=null;
		try{
			session=SessionFactoryUtil.getInstance().openSession();
			tx=session.beginTransaction();
			String updateQuery = "Update tbProductionStep set stepName = '"+txtSubGroupName.getValue().toString().trim()+"', " +
					"ProductionTypeId='"+cmbGroupName.getValue()+"',UserName ='"+sessionBean.getUserName()+"'," +
					" UserIP='"+sessionBean.getUserIp()+"', EntryTime= CURRENT_TIMESTAMP where " +
					"stepId = '"+trSubGroupId.getValue().toString().trim()+"'";
			session.createSQLQuery(updateQuery).executeUpdate();
		}
		catch(Exception exp){
			showNotification(""+exp,Notification.TYPE_ERROR_MESSAGE);
			tx.rollback();
		}

		finally{
			if(session!=null||tx!=null){
				tx.commit();
				session.close();
				this.getParent().showNotification("All Information Save successfully.");
				btnIni(true);
			}
		}
	}

	private void saveButtonEvent() 
	{
		if(cmbGroupName.getValue()!=null)
		{
			if(isUpdate)
			{
				final MessageBox mb = new MessageBox(getParent(), "Are you sure?", MessageBox.Icon.QUESTION, "Do you want to update product information?", new MessageBox.ButtonConfig(MessageBox.ButtonType.YES, "Yes"), new MessageBox.ButtonConfig(MessageBox.ButtonType.NO, "No"));
				mb.show(new EventListener()
				{
					public void buttonClicked(ButtonType buttonType)
					{
						if(buttonType == ButtonType.YES)
						{
							mb.buttonLayout.getComponent(0).setEnabled(false);
							updateData();
							button.btnNew.focus();
							isUpdate=false;
							componentIni(true);
							txtClear();
							btnIni(true);
						}
					}
				});		
			}
			else
			{
				final MessageBox mb = new MessageBox(getParent(), "Are you sure?", MessageBox.Icon.QUESTION, "Do you want to Save information?", new MessageBox.ButtonConfig(MessageBox.ButtonType.YES, "Yes"), new MessageBox.ButtonConfig(MessageBox.ButtonType.NO, "No"));
				mb.show(new EventListener()
				{
					public void buttonClicked(ButtonType buttonType)
					{
						if(buttonType == ButtonType.YES)
						
						{
							mb.buttonLayout.getComponent(0).setEnabled(false);
							insertData();
							btnIni(true);
							cmbCategoryValueAdd();
							componentIni(true);
							txtClear();
							btnIni(true);
							button.btnNew.focus();
						}
					}
				});		
			}
		}

		else
		{
			this.getParent().showNotification("Warning :", "Please select Category Name", Notification.TYPE_WARNING_MESSAGE);
			cmbGroupName.focus();
		}
	}

	private void insertData()
	{
		Transaction tx=null;
		Session session=null;
		try{
			session=SessionFactoryUtil.getInstance().openSession();
			tx=session.beginTransaction();
			String insertQuery = "Insert Into tbProductionStep (StepId,StepName,productionTypeId,UserName,UserIP,EntryTime) values " +
					"('"+trSubGroupId.getValue().toString().trim()+"','"+txtSubGroupName.getValue().toString().trim()+"'," +
					"'"+cmbGroupName.getValue()+"','"+sessionBean.getUserName()+"','"+sessionBean.getUserIp()+"'," +
					"CURRENT_TIMESTAMP)";
			session.createSQLQuery(insertQuery).executeUpdate();
		}
		catch(Exception exp){
			showNotification(""+exp,Notification.TYPE_ERROR_MESSAGE);
			tx.rollback();
		}

		finally{
			if(session!=null||tx!=null){
				tx.commit();
				session.close();
				this.getParent().showNotification("All Information Save successfully.");
				btnIni(true);
			}
		}
	}

	private void newButtonEvent() 
	{
		componentIni(false);
		btnIni(false);
		txtClear();
		cmbCategoryValueAdd();
		trSubGroupId.setEnabled(false);	
	}
	private Iterator dbService(String sql){
		Transaction tx=null;
		Session session=null;
		try{
			session=SessionFactoryUtil.getInstance().openSession();
			tx=session.beginTransaction();
			return session.createSQLQuery(sql).list().iterator();
		}
		catch(Exception exp){
			tx.rollback();
			showNotification(""+exp,Notification.TYPE_ERROR_MESSAGE);
		}
		finally{
			if(tx!=null||session!=null){
				tx.commit();
				session.close();
			}
		}
		return null;
	}
	public void cmbCategoryValueAdd()
	{
		cmbGroupName.removeAllItems();
		String sql="select productTypeId,ProductTypeName from tbProductionType";
		Iterator<?>iter=dbService(sql);
		while(iter.hasNext()){
			Object[] element = (Object[]) iter.next();
			cmbGroupName.addItem(element[0]);
			cmbGroupName.setItemCaption(element[0], (String) element[1]);
		}
	}
