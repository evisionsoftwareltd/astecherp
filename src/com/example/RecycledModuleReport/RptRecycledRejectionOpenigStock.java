	private void allButtonAction()
	{	
		
		previewButton.addListener(new Button.ClickListener() 
		{
			public void buttonClick(ClickEvent event) {
				if(cmbProductName.getValue()!=null  || chkAllProductName.booleanValue()==true){
					
					reportShow();
				}
				else{
					getParent().showNotification("Warning!","Please select item name.", Notification.TYPE_WARNING_MESSAGE);
				}
				
			}
		});

		chkAllProductName.addListener(new ValueChangeListener()
		{
			public void valueChange(ValueChangeEvent event)
			{
				boolean bv = chkAllProductName.booleanValue();
				if(bv==true)
				{
					cmbProductName.setValue(null);
					cmbProductName.setEnabled(false);
					}	
					else{
						cmbProductName.setEnabled(true);

						cmbProductName.setValue(null);
						chkAllProductName.setValue(false);
						addSectionName();
					}	
			}
		});
		exitButton.addListener( new Button.ClickListener() 
		{
			public void buttonClick(ClickEvent event) 
			{
				close();
			}
		});
		
		chkpdf.addListener(new ValueChangeListener()
		{
			
			public void valueChange(ValueChangeEvent event)
			{
				
				if(chkpdf.booleanValue()==true)
					chkother.setValue(false);
				else
					chkother.setValue(true);
				
			}
		});

		chkother.addListener(new ValueChangeListener()
		{
			
			public void valueChange(ValueChangeEvent event)
			{
				
				if(chkother.booleanValue()==true)
					chkpdf.setValue(false);
				else
					chkpdf.setValue(true);
				
			}
		});
	}
	public void addSectionName()
	{
		cmbProductName.removeAllItems();
		//cmbProductName.addItem("All");
		Transaction tx = null;
		try{
			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			tx = session.beginTransaction();
			//List list = session.createSQLQuery("select * from tbSectionInfo").list();
			String query = "select distinct vProductId,vProductName from tbRejectedOpening";
			List list = session.createSQLQuery(query).list();
			System.out.println("Section List:"+list.size());

			for(Iterator iter=list.iterator();iter.hasNext();)
			{
				Object[] element = (Object[]) iter.next();
				cmbProductName.addItem(element[0].toString());
				cmbProductName.setItemCaption(element[0].toString(), element[1].toString());
			}
		}catch(Exception exp){
			this.getParent().showNotification("Error",exp+"",Notification.TYPE_ERROR_MESSAGE);
		}
	}


	private void reportShow()
	{
		String query=null;
		String query1=null;
		String activeFlag = null;
		String ProductionName= "";

		if (cmbProductName.getValue()!= null)
		{
			ProductionName = cmbProductName.getValue().toString();
		}
		else
		{
			ProductionName="%";
		}
		
		if(chkpdf.booleanValue()==true)
			type=true;
		else
			type=false;

		try{

			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			Transaction tx = session.beginTransaction();
			HashMap hm = new HashMap();

			hm.put("logo", sessionBean.getCompanyLogo());
			hm.put("company", sessionBean.getCompany());
			hm.put("address", sessionBean.getCompanyAddress());
			hm.put("Phone", sessionBean.getCompanyContact());
			hm.put("UserName", sessionBean.getUserName()+"  "+sessionBean.getUserIp());
			hm.put("openingYear", new SimpleDateFormat("yyyy").format(OpeningYear.getValue()));
			//hm.put("toDate",new SimpleDateFormat("dd-MM-yyyy").format(toDate.getValue()));
			hm.put("section", cmbProductName.getItemCaption(cmbProductName.getValue()));

			
				query= "select iTransactionId,dTransactionDate,openingYear,vProductId,vProductName,mQtykg,mQtyPcs, "
						+ "iStoreId,vStoreName,vUnitName,vColor from tbRejectedOpening "
						+ " where YEAR(openingYear)='"+new SimpleDateFormat("yyyy").format(OpeningYear.getValue())+"' "
						+ "and vProductId like '"+ProductionName+"' "	;
					
					
			System.out.println(query);
			hm.put("sql", query);
			
			List lst=session.createSQLQuery(query).list();
			
			if(!lst.isEmpty())
			{
				Window win = new ReportViewer(hm,"report/production/RecycledModuleReport/RptRejectionOpeningStock.jasper",
					this.getWindow().getApplication().getContext().getBaseDirectory()+"".replace("\\","/")+"/VAADIN/rpttmp",
					this.getWindow().getApplication().getURL()+"VAADIN/rpttmp",false,
					this.getWindow().getApplication().getURL()+"VAADIN/applet",type);
				win.setCaption("Project Report :: "+sessionBean.getCompany());

				this.getParent().getWindow().addWindow(win);
			}
			
			else
			{
				
				getParent().showNotification("Date Not Found", Notification.TYPE_WARNING_MESSAGE);
				
			}
			
		}
		catch(Exception exp){

			this.getParent().showNotification("Error "+exp,Notification.TYPE_ERROR_MESSAGE);

		}
	}

	

