	private void saveButtonEvent() 
	{
		if(cmbcategoryType.getValue()!=null)
		{
			if(cmbCategoryName.getValue()!=null)
			{
				if(isUpdate)
				{
					final MessageBox mb = new MessageBox(getParent(), "Are you sure?", MessageBox.Icon.QUESTION, "Do you want to update product information?", new MessageBox.ButtonConfig(MessageBox.ButtonType.YES, "Yes"), new MessageBox.ButtonConfig(MessageBox.ButtonType.NO, "No"));
					mb.show(new MessageBox.EventListener()
					{
						public void buttonClicked(ButtonType buttonType)
						{
							if(buttonType == ButtonType.YES)
							{
								mb.buttonLayout.getComponent(0).setEnabled(false);
								updateData();
								button.btnNew.focus();
								isUpdate=false;
								componentIni(true);
								txtClear();
								btnIni(true);
								mb.close();
							}
						}
					});		
				}
				else
				{
					final MessageBox mb = new MessageBox(getParent(), "Are you sure?", MessageBox.Icon.QUESTION, "Do you want to Save information?", new MessageBox.ButtonConfig(MessageBox.ButtonType.YES, "Yes"), new MessageBox.ButtonConfig(MessageBox.ButtonType.NO, "No"));
					mb.show(new MessageBox.EventListener()
					{
						public void buttonClicked(ButtonType buttonType)
						{
							if(buttonType == ButtonType.YES)
							{
								mb.buttonLayout.getComponent(0).setEnabled(false);
								insertData();
								btnIni(true);
								cmbCategoryValueAdd();
								componentIni(true);
								txtClear();
								btnIni(true);
								button.btnNew.focus();
								mb.close();
							}
						}
					});		
				}
			}

			else
			{
				this.getParent().showNotification("Warning :", "Please select Category Name", Notification.TYPE_WARNING_MESSAGE);
				cmbCategoryName.focus();
			}	
		}
		
		else
		{
			this.getParent().showNotification("Warning :", "Please select Category Type", Notification.TYPE_WARNING_MESSAGE);
			cmbCategoryName.focus();
		}
		
		
	}

	private void insertData()
	{
		Transaction tx = null;
		String subGroupId = "";
		try
		{
			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			tx = session.beginTransaction();

			List lst = session.createSQLQuery("select 0 zero,('S'+ISNULL((SELECT cast((max(substring(Sub_Group_Id,2,len(Group_Id)-1))+1)" +
					" as VARCHAR) FROM tbSub_Group),101)) id").list();

			if (lst.iterator().hasNext()) 
			{
				Object[] element = (Object[]) lst.iterator().next();

				subGroupId = element[1].toString();
			}

			String insertQuery = "Insert Into tbRawItemSubCategory (iSubCategoryID,vSubCategoryName,iCategoryID," +
					" Group_Id,SubGroup_Id,vUserName,vUserIP,dtEntryTime,vCategoryType) values" +
					" ('"+trSubcategoryId.getValue().toString().trim()+"'," +
					" '"+txtSubcategoryName.getValue().toString().trim()+"'," +
					"( select iCategoryCode from tbRawItemCategory where iCategoryCode like '"+cmbCategoryName.getValue()+"')," +
					" (Select Group_Id From tbRawItemCategory where iCategoryCode='"+cmbCategoryName.getValue()+"')," +
					" '"+subGroupId+"'," +
					" '"+sessionBean.getUserName()+"'," +
					" '"+sessionBean.getUserIp()+"',CURRENT_TIMESTAMP,'"+cmbcategoryType.getValue()+"')";

			/*String sql = "INSERT INTO tbSub_Group(Sub_Group_Id,Sub_Group_Name,Group_Id,userId,userIp,entryTime)" +
					" VALUES( '"+subGroupId+"', " +
					" '"+txtSubcategoryName.getValue()+"'," +
					" (Select Group_Id From tbRawItemCategory where Group_Id='"+cmbCategoryName.getValue()+"')," +
					" '"+sessionBean.getUserId()+"'," +
					" '"+sessionBean.getUserIp()+"',CURRENT_TIMESTAMP)";

			session.createSQLQuery(sql).executeUpdate();*/
			session.createSQLQuery(insertQuery).executeUpdate();

			tx.commit();

			this.getParent().showNotification("All information Save successfully.");
			btnIni(true);
		}
		catch(Exception exp)
		{
			this.getParent().showNotification("Error",exp+"",Notification.TYPE_ERROR_MESSAGE);
			tx.rollback();
		}
	}

