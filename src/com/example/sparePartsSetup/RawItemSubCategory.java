	private void findInitialise(String txtSubCatogoryId) 
	{
		Transaction tx = null;
		try 
		{
			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			tx = session.beginTransaction();
			
			String query="";
			query=" select iSubCategoryID,vSubCategoryName,vCategoryType,iCategoryID,SubGroup_Id from tbRawItemSubCategory Where iSubCategoryID = '"+txtSubCatogoryId+"'  ";

			List led = session.createSQLQuery(query).list();

			if (led.iterator().hasNext()) 
			{
				Object[] element = (Object[]) led.iterator().next();

				trSubcategoryId.setValue(element[0]);
				txtSubcategoryName.setValue(element[1]);
				cmbcategoryType.setValue(element[2].toString());
				cmbCategoryName.setValue(element[3]);

				findSubGroupId = element[4].toString();
			}
		}
		catch (Exception exp) 
		{
			this.getParent().showNotification("Error", exp + "",Notification.TYPE_ERROR_MESSAGE);
			tx.rollback();
		}
	}

