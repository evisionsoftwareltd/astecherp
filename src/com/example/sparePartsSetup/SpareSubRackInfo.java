	public void cmbRackAdd()
	{
		cmbRackName.removeAllItems();
		try{
	
			String sql="Select vRackId,vRackName from tbRackInformation order by vRackName";
			Iterator<?> iter=dbService(sql);
			while(iter.hasNext())
			{
				Object[] element = (Object[]) iter.next();
				cmbRackName.addItem(element[0]);
				cmbRackName.setItemCaption(element[0],  element[1].toString());
			}
		}
		catch(Exception exp)
		{
			showNotification("Error !",exp+"",Notification.TYPE_ERROR_MESSAGE);
		}
	}
	
