	private void findInitialise(String strtxtSubRackId) 
	{
		Transaction tx = null;
		try 
		{
			Session session = SessionFactoryUtil.getInstance().getCurrentSession();
			tx = session.beginTransaction();
			
			String query="";
			query=" SElect vRackId,vRackName,vDepoId,vDepoName,vSubRackId,vSubRackName from tbSubRackInformation Where vSubRackId = '"+strtxtSubRackId+"'  ";

			List led = session.createSQLQuery(query).list();

			if (led.iterator().hasNext()) 
			{
				Object[] element = (Object[]) led.iterator().next();
				
				cmbRackName.addItem(element[0]);
				cmbRackName.setValue(element[0]);
				txtStoreId.setValue(element[1]);
				txtStoreName.setValue(element[2]);
				txtSubRackId.setValue(element[4]);
				txtSubRackName.setValue(element[5]);
				//findRackId = element[0].toString();
			}
		}
		catch (Exception exp) 
		{
			showNotification("Error", exp + "",Notification.TYPE_ERROR_MESSAGE);
			tx.rollback();
		}
	}

